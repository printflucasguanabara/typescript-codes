let x = 6;
let y = 10;

let eMaiorQue = y > x;
console.log(eMaiorQue); // Exibe true

let eMenorQue = x < y;
console.log(eMenorQue); // Exibe true

let eMaiorOuIgual = x >= y; // 6 >= 10
console.log(eMaiorOuIgual); // Exibe false

let eMenorOuIgual = x <= y; // 6 <= 10
console.log(eMenorOuIgual); // Exibe true
